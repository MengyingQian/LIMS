package com.ddxq.common.log;

import java.io.Serializable;

/** 
* @author jkc  E-mail: 754438390@qq.com
* @version 创建时间：2016年5月3日 下午6:05:15 
* 类说明 
*/
public class DaoLogPojo implements Serializable {


	private String time;//时间 "time"
	private String act_level;//操作级别（DEBUG，INFO，ERROR） "act_level"
	private String action_type;//操作类型  "action_type"
	private String data_name;//库名称
	private String server_IP;//服务器IP地址  "server_IP"
	private String server_port;//服务器端口 "server_port"
	private String data_IP;//连接库的IP地址,dao层
	private String data_port;//库端口
	private String action_info;//操作信息  "action_info" 此处存取的是传过来的className	
	private String action_desc;//操作描述  "action_desc"//干了什么事比如用户登录，
	private String action_language;//操作语句，只限于dao层
	private String action_result;//操作结果（成功、失败、延迟） "action_result"
	private String action_time;//操作执行时间
	private String nickname;//用户名   "nickname"	
	private String userid;//用户id "userid"
	private String money;//钱数  "money"
	
	
	public DaoLogPojo(String time, String act_level, String action_type, String data_name, String server_IP,
			String server_port, String data_IP, String data_port, String action_info, String action_desc,
			String action_language, String action_result, String action_time, String nickname, String userid,
			String money) {
		super();
		this.time = time;
		this.act_level = act_level;
		this.action_type = action_type;
		this.data_name = data_name;
		this.server_IP = server_IP;
		this.server_port = server_port;
		this.data_IP = data_IP;
		this.data_port = data_port;
		this.action_info = action_info;
		this.action_desc = action_desc;
		this.action_language = action_language;
		this.action_result = action_result;
		this.action_time = action_time;
		this.nickname = nickname;
		this.userid = userid;
		this.money = money;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getAct_level() {
		return act_level;
	}
	public void setAct_level(String act_level) {
		this.act_level = act_level;
	}
	public String getAction_type() {
		return action_type;
	}
	public void setAction_type(String action_type) {
		this.action_type = action_type;
	}
	public String getData_name() {
		return data_name;
	}
	public void setData_name(String data_name) {
		this.data_name = data_name;
	}
	public String getServer_IP() {
		return server_IP;
	}
	public void setServer_IP(String server_IP) {
		this.server_IP = server_IP;
	}
	public String getServer_port() {
		return server_port;
	}
	public void setServer_port(String server_port) {
		this.server_port = server_port;
	}
	public String getData_IP() {
		return data_IP;
	}
	public void setData_IP(String data_IP) {
		this.data_IP = data_IP;
	}
	public String getData_port() {
		return data_port;
	}
	public void setData_port(String data_port) {
		this.data_port = data_port;
	}
	public String getAction_info() {
		return action_info;
	}
	public void setAction_info(String action_info) {
		this.action_info = action_info;
	}
	public String getAction_desc() {
		return action_desc;
	}
	public void setAction_desc(String action_desc) {
		this.action_desc = action_desc;
	}
	public String getAction_language() {
		return action_language;
	}
	public void setAction_language(String action_language) {
		this.action_language = action_language;
	}
	public String getAction_result() {
		return action_result;
	}
	public void setAction_result(String action_result) {
		this.action_result = action_result;
	}
	public String getAction_time() {
		return action_time;
	}
	public void setAction_time(String action_time) {
		this.action_time = action_time;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	
	
	


}

package com.ddxq.common.log;

import java.io.Serializable;

/** 
* @author jkc  E-mail: 754438390@qq.com
* @version 创建时间：2016年5月3日 下午6:05:30 
* 类说明 
*/
public class ErrorLogPojo implements Serializable{
	
	
    private String time;//时间 "time"
	private String server_IP;//服务器IP地址  "server_IP"
	private String server_port;//服务器端口 "server_port"
	private String action_info;//操作信息  "action_info" 此处存取的是传过来的className	
	private String action_desc;//操作描述  "action_desc"//干了什么事比如用户登录，
	private String nickname;//用户名   "nickname"	
	private String userid;//用户id "userid"
	private String error_massage;//异常信息，error层	
	
	public ErrorLogPojo(String time, String server_IP, String server_port, String action_info, String action_desc,
			String nickname, String userid, String error_massage) {
		super();
		this.time = time;
		this.server_IP = server_IP;
		this.server_port = server_port;
		this.action_info = action_info;
		this.action_desc = action_desc;
		this.nickname = nickname;
		this.userid = userid;
		this.error_massage = error_massage;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getServer_IP() {
		return server_IP;
	}
	public void setServer_IP(String server_IP) {
		this.server_IP = server_IP;
	}
	public String getServer_port() {
		return server_port;
	}
	public void setServer_port(String server_port) {
		this.server_port = server_port;
	}
	public String getAction_info() {
		return action_info;
	}
	public void setAction_info(String action_info) {
		this.action_info = action_info;
	}
	public String getAction_desc() {
		return action_desc;
	}
	public void setAction_desc(String action_desc) {
		this.action_desc = action_desc;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getError_massage() {
		return error_massage;
	}
	public void setError_massage(String error_massage) {
		this.error_massage = error_massage;
	}
	


}
